export function detectLang() {
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone
    if (tz === 'Asia/Tokyo') return 'ja'
  } catch (_) {}
  const nav = navigator.language || ''
  return nav.startsWith('ja') ? 'ja' : 'en'
}

